package com.jpexs.asdec.types;

/**
 * Represents 32-bit alpha, red, green and blue value
 *
 * @author JPEXS
 */
public class ARGB {
    /**
     * Alpha value defining opacity
     */
    public int alpha;
    /**
     * Red color value
     */
    public int red;
    /**
     * Green color value
     */
    public int green;
    /**
     * Blue color value
     */
    public int blue;
}