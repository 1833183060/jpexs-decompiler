package com.jpexs.asdec.types;

/**
 * Represents 32-bit red, green, blue and alpha value
 *
 * @author JPEXS
 */
public class RGBA {
    /**
     * Red color value
     */
    public int red;
    /**
     * Green color value
     */
    public int green;
    /**
     * Blue color value
     */
    public int blue;
    /**
     * Alpha value defining opacity
     */
    public int alpha;
}
